/* Возвращает указатель на таблицу для заданной кодировки */
static const uint32_t* get_mapping(const char* encoding) {
    if (strcmp(encoding, "CP1251") == 0 || strcmp(encoding, "cp1251") == 0)
        return cp1251_table;
    if (strcmp(encoding, "KOI8-R") == 0 || strcmp(encoding, "koi8-r") == 0)
        return koi8r_table;
    if (strcmp(encoding, "ISO-8859-5") == 0 || strcmp(encoding, "iso-8859-5") == 0)
        return iso8859_5_table;
    return NULL;
}

/* Записывает код Unicode в выходной поток в кодировке UTF-8 */
static void write_utf8(FILE* out, uint32_t cp) {
    if (cp <= 0x7F) {
        fputc((int)cp, out);
    } else if (cp <= 0x7FF) {
        fputc(0xC0 | (int)((cp >> 6) & 0x1F), out);
        fputc(0x80 | (int)(cp & 0x3F), out);
    } else if (cp <= 0xFFFF) {
        fputc(0xE0 | (int)((cp >> 12) & 0x0F), out);
        fputc(0x80 | (int)((cp >> 6) & 0x3F), out);
        fputc(0x80 | (int)(cp & 0x3F), out);
    } else if (cp <= 0x10FFFF) {
        fputc(0xF0 | (int)((cp >> 18) & 0x07), out);
        fputc(0x80 | (int)((cp >> 12) & 0x3F), out);
        fputc(0x80 | (int)((cp >> 6) & 0x3F), out);
        fputc(0x80 | (int)(cp & 0x3F), out);
    } else {
        /* Некорректный код – записываем U+FFFD */
        fputc(0xEF, out);
        fputc(0xBF, out);
        fputc(0xBD, out);
    }
}

/* ------------------------------------------------------------
   Основная функция
   ------------------------------------------------------------ */
int main(int argc, char** argv) {
    if (argc != 4) {
        fprintf(stderr, "Usage: %s <input_file> <encoding> <output_file>\n", argv[0]);
        fprintf(stderr, "  encoding: CP1251, KOI8-R, ISO-8859-5\n");
        return EXIT_FAILURE;
    }

    const char* input_path  = argv[1];
    const char* encoding    = argv[2];
    const char* output_path = argv[3];

    /* Получаем таблицу перекодировки */
    const uint32_t* mapping = get_mapping(encoding);
    if (!mapping) {
        fprintf(stderr, "Error: unknown encoding '%s'\n", encoding);
        fprintf(stderr, "Supported: CP1251, KOI8-R, ISO-8859-5\n");
        return EXIT_FAILURE;
    }

    /* Открываем входной файл в бинарном режиме */
    FILE* in = fopen(input_path, "rb");
    if (!in) {
        fprintf(stderr, "Error: cannot open input file '%s'\n", input_path);
        return EXIT_FAILURE;
    }

    /* Открываем выходной файл в бинарном режиме */
    FILE* out = fopen(output_path, "wb");
    if (!out) {
        fprintf(stderr, "Error: cannot open output file '%s'\n", output_path);
        fclose(in);
        return EXIT_FAILURE;
    }

    /* Читаем байт за байтом и преобразуем */
    int c;
    while ((c = fgetc(in)) != EOF) {
        unsigned char byte = (unsigned char)c;
        uint32_t cp;

        if (byte < 0x80) {
            cp = byte;                 /* ASCII */
        } else {
            cp = mapping[byte];        /* по таблице */
        }
        write_utf8(out, cp);
    }

    /* Проверяем ошибки ввода-вывода */
    if (ferror(in)) {
        fprintf(stderr, "Error: input file read error\n");
        fclose(in);
        fclose(out);
        return EXIT_FAILURE;
    }
    if (ferror(out)) {
        fprintf(stderr, "Error: output file write error\n");
        fclose(in);
        fclose(out);
        return EXIT_FAILURE;
    }

    fclose(in);
    fclose(out);

    return EXIT_SUCCESS;
}